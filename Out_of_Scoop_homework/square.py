import turtle

# Create a turtle object
t = turtle.Turtle()

# Draw the first side (width)
t.forward(200)    # Move forward by 150 units (width)

# Turn 90 degrees to the left
t.left(90)

# Draw the second side (height)
t.forward(200)    # Move forward by 100 units (height)

# Turn 90 degrees to the left
t.left(90)

# Draw the third side (width)
t.forward(200)    # Move forward by 150 units (width)

# Turn 90 degrees to the left
t.left(90)

# Draw the fourth side (height)
t.forward(200)    # Move forward by 100 units (height)

# Keep the window open
turtle.done()
